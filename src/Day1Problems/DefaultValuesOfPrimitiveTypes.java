package Day1Problems;

public class DefaultValuesOfPrimitiveTypes {
    static byte x;
    static short s;
    static int i;
    static long l;
    static float f;
    static double d;
    static char c;
    static boolean b;
    public static void main(String[] args){
        System.out.println("Default value of Byte Type: "+x);
        System.out.println("Default value of Short Type: "+s);
        System.out.println("Default value of Integer Type: "+i);
        System.out.println("Default value of Long Type: "+l);
        System.out.println("Default value of Float Type: "+f);
        System.out.println("Default value of Double Type: "+d);
        System.out.println("Default value of Char Type: "+c);
        System.out.println("Default value of Boolean Type: "+b);
    }
}
